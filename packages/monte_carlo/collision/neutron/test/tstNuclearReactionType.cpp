// Check that a raw mt number can be converted to a nuclear reaction
FRENSIE_UNIT_TEST(NuclearReactionType, convertMTNumberToNuclearReactionType) {
  for (auto reaction : reaction_idx_map) {
    MonteCarlo::NuclearReactionType type =
        MonteCarlo::convertMTNumberToNuclearReactionType(reaction.second);
    FRENSIE_CHECK_EQUAL(type, reaction.first);
  }
}

//---------------------------------------------------------------------------//
// Check that a nuclear reaction type can be converted to a string
FRENSIE_UNIT_TEST(NuclearReactionType, toString) {
  for (auto reaction : reaction_name_map) {
    std::string type_string = Utility::toString(reaction.first);
    FRENSIE_CHECK_EQUAL(type_string, reaction.second);
  }
}

//---------------------------------------------------------------------------//
// Check that a nuclear reaction type can be placed in a stream
FRENSIE_UNIT_TEST(NuclearReactionType, ostream_operator) {
  for (auto reaction : reaction_name_map) {
    std::ostringstream oss;
    oss << reaction.first;
    FRENSIE_CHECK_EQUAL(oss.str(), reaction.second);
  }
}

//---------------------------------------------------------------------------//
// Check that a nuclear reaction type can be archived
FRENSIE_UNIT_TEST_TEMPLATE_EXPAND(NuclearReactionType, archive, TestArchives) {
  FETCH_TEMPLATE_PARAM(0, RawOArchive);
  FETCH_TEMPLATE_PARAM(1, RawIArchive);

  typedef typename std::remove_pointer<RawOArchive>::type OArchive;
  typedef typename std::remove_pointer<RawIArchive>::type IArchive;

  std::string archive_base_name("test_nuclear_reaction_type");
  std::ostringstream archive_ostream;

  {
    std::unique_ptr<OArchive> oarchive;
    createOArchive(archive_base_name, archive_ostream, oarchive);

    for (auto reaction : reaction_name_map) {
      MonteCarlo::NuclearReactionType type = reaction.first;
      FRENSIE_REQUIRE_NO_THROW((*oarchive) << BOOST_SERIALIZATION_NVP(type));
    }
  }

  {
    std::ofstream ofs("test.out");
    ofs << archive_ostream.str();
    ofs.close();
  }

  // Copy the archive ostream to an istream
  std::istringstream archive_istream(archive_ostream.str());

  // Load the archived distributions
  std::unique_ptr<IArchive> iarchive;

  createIArchive(archive_istream, iarchive);

  for (auto reaction : reaction_name_map) {
    MonteCarlo::NuclearReactionType type;
    FRENSIE_REQUIRE_NO_THROW((*iarchive) >> BOOST_SERIALIZATION_NVP(type));
    FRENSIE_CHECK_EQUAL(type, reaction.first);
  }
  iarchive.reset();
}

